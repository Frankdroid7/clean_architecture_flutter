class CountryIdentifierUIModel {
  final bool name;
  final bool capital;
  final bool language;
  final bool code;
  final bool currency;
  final bool region;
  final bool subregion;
  final bool demonym;
  final bool translation;

  const CountryIdentifierUIModel({
    required this.name,
    required this.capital,
    required this.language,
    required this.code,
    required this.currency,
    required this.region,
    required this.subregion,
    required this.demonym,
    required this.translation,
  });
}
